function formatDate(timestamp) {
  let days = [
    "Sunday",
    "Monday",
    "Tuesday",
    "Wednesday",
    "Thursday",
    "Friday",
    "Saturday",
  ];
  let date = new Date(timestamp);
  let hour = date.getHours();
  let minutes = date.getMinutes();
  let day = days[date.getDay()];
  let currentDate = `${day} ${hour}:${minutes}`;
  return currentDate;
}
